            <h3 class="page-header">Conceptual Underpinnings</h3>
            <p>
                <b>Learning Design Studio<sup>HE</sup> (LDS<sup>HE</sup>) is a pedagogically grounded productivity and collaboration platform for professionals in the Learning Design (LD),
                Learning Analytics (LA), and Education communities</b> (particularly those interested in using LD to support Teacher Inquiry of Student Learning (TISL)).
                To do so requires a <b>common design language</b> that can (1) capture well-constructed pedagogical practices and the underpinning learning design principles,
                as well as specify the necessary learning analytics appropriate for the intended learning outcomes and chosen pedagogy, and (2) be understood by practitioners
                and researchers in all of the three targeted communities.
            </p>
            <p>
                To achieve this goal, an important part of the R&D effort in this project is to develop a pattern language,
                which has been greatly inspired by both the outcome-based educational approach (OBE) (Harden, 2002; King & Evans, 1991),
                and the Alexandrian pattern language (Alexander, 1964, 1979; Alexander, Ishikawa, & Silverstein, 1977).
                The LDS pattern language differs from the Alexandrian pattern language in that it provides a formalism (or language)
                which can be used to construct design patterns at different levels of granularity in learning design (Law et al., 2017)
                such the number and characteristics of the patterns that can be constructed is not limited to a fixed number as in the case of the Alexandrian pattern language.
                This pattern language is being extended such that it can be used to specify LA tools and visualizations for LD patterns represented in the pattern language.
            </p>
            <p>
                The <b>LDS pattern language</b> facilitates design thinking as well as provide strategies and tools that foster and support designs that align with the following <b>design principles:</b>
                <ul>
                    <li>
                        The learning tasks should be designed to provide learners with experiences that are more likely to bring about the targeted learning outcome goals;
                    </li>
                    <li>
                        Learning designs should promote student agency for self-directed learning, encourage student sharing and collaboration;
                    </li>
                    <li>
                        Good assessment designs should make learning outcomes visible, and would make more contribution to learning if assessment is integrated as an integral part of the learning process, and the assessment criteria are made explicit;
                    </li>
                    <li>
                        Appropriate, just-in-time feedback should be designed to promote learning;
                    </li>
                    <li>
                        The learning environment should empower learners by providing them with tools and resources needed to communicate and construct;
                    </li>
                    <li>
                        Learning designs should focus on learners' experiences rather than the work of the teacher or instructional designer;
                    </li>
                    <li>
                        Learning is primarily social, hence LD needs to take account of the social organization of learning, including the management aspects such as the formation/assignment of groups;
                    </li>
                    <li>
                        The learning environment, including the social, physical and digital dimensions are integral parts of the design.
                    </li>
                </ul>
            </p>
            <p>
                <b>LDS<sup>HE</sup></b> start with a course as the highest level <b>granularity of design</b> in the LDS pattern language. The course level design sets out the overall course structure in terms of the intended learning outcome objectives,
                as well as the total learning time and the time allocated to each session. The learning unit level design specifies the set of learning units needed to achieve the course outcomes.
                Each learning unit comprises task sequences organized in the form of design patterns according to explicit pedagogical design principles that addresses a particular type or constellation of learning outcomes.
                Pedagogy/pedagogical approaches are realized through the learning design patterns encapsulated in the learning units. Finally, the learning task level design specifies the context, duration, tool(s), and resource(s) needed for satisfactory implementation of the task concerned.
                This hierarchically nested design sequence follows the 'living structure' concept in Alexander's pattern language, going from larger structures to smaller structures, to embellishments of those structures.
                The multilevel, hierarchically nested design structures provide teachers and other involved designers with easy navigation and control during the complex design process,
                allowing them to zoom in or out easily to focus on the design work at different levels from the entire course to detailed design of a specific learning resource without losing context and coherence.
            </p>
